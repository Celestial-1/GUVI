# Write a program to check whether a person is eligible for voting or not. If age is greater than or equal to 18 display “yes” else “no”

# Input Description:
# First line consists of input number

# Output Description:
# Output consists of “yes” or “no”

# Sample Input :
# 18
# Sample Output :
# yes



# Get age as input
age = int(input())

# Check eligibility
if age >= 18:
    print("yes")
else:
    print("no")
